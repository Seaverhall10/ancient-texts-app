                        "world you will have tribulation' (John 16:33). 'If they persecuted me, "
                        "they will also persecute you' (John 15:20). 'The one who endures to "
                        "the end will be saved' (Matthew 24:13). Paul tells new converts that "
                        "'through many tribulations we must enter the kingdom of God' (Acts "
                        "14:22). The apostolic expectation was suffering before glory. The "
                        "rapture inverts this: glory before suffering -- or rather, glory "
                        "instead of suffering. It is precisely the kind of teaching Paul warned "
                        "Timothy about: people accumulating teachers to suit their own desires, "
                        "turning from truth to myths (2 Timothy 4:3-4). Not because rapture "
                        "teachers are malicious, but because the doctrine appeals to the "
                        "deepest human desire -- to be spared from pain -- and baptizes it as "
                        "biblical promise."
